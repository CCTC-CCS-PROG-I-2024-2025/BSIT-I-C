public class CANTORNE_ACTIVITY_IV {
    public static void main(String [] args){
        int x = 7;
        int z = 3;
        int p = 10;
        
        int sum = x+z+p;
        int diff = x-z-p;
        int prod = x*z*p;
        int qoute = x/z/p;

        System.out.println(sum);
        System.out.println(diff);
        System.out.println(prod);
        System.out.println(qoute);
    }
}
