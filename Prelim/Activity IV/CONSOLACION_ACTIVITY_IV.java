public class CONSOLACION_ACTIVITY_IV{
    public static void main(String [] args){
        int  a = 6;
        int b = 12;
        int c = 8;

        int sum = a+b+c;
        int diff = a-b-c;
        int prod = a*b*c;
        int qoute = a/b/c;

        System.out.println(sum);
        System.out.println(diff);
        System.out.println(prod);
        System.out.println(qoute);
    }
}