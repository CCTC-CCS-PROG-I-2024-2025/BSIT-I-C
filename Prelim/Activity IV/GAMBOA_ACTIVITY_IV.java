 class GAMBOA_ACTIVITY_IV {
    public static void main(String[] args) {
    int bats = 20;
    int helemets = 18;
    int gloves = 24;
    int sum = bats + helemets + gloves;
    int difference = bats - helemets - gloves;
    int product = bats * helemets * gloves;
    int quotient = bats / helemets / gloves;
    System.out.println(sum);
    System.out.println(difference);
    System.out.println(product);
    System.out.println(quotient);
    }
}
