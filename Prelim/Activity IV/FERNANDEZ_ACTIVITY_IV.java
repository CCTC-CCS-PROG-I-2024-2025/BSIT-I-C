class FERNANDEZ_ACTIVITY_IV {
    public static void main(String[] args) {
        int x = 10;
        int y = 5;
        int a = 18;
        int sum = x + y + a;
        int difference = x - y - a;
        int product = x * y * a;
        int quotient = x / y / a;
        System.out.println(sum);
        System.out.println(difference);
        System.out.println(product);
        System.out.println(quotient);
    }
}