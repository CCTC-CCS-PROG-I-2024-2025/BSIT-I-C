public class BELVESTRE_ACTIVITY_IV {
    public static void main(String[] args) {
        int a = 10;
        int b = 35;
        int c = 50;

        int sum = a+b+c;
        int difference = a - b - c;
        int product = a * b * c;
        int quotient = a / b / c;

        System.out.println(sum);
        System.out.println(difference);
        System.out.println(product);
        System.out.println(quotient);
    }
    
}
