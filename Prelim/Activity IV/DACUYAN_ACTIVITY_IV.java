public class DACUYAN_ACTIVITY_IV {
    public static void main(String[] args) {
        int e = 10;
        int d = 5;
        int c = 15;
        
        int sum = e + d + c;
        int diff = e - d - c;
        int prod = e * d * c;
        int quot = e / d / c;

        System.out.println(sum);
        System.out.println(diff);
        System.out.println(prod);
        System.out.println(quot);

    }
}